/// Auto-discovers crypto subdirectories containing headers.
/// Excludes hidden/, compat/, arch/ — these are handled separately in the
/// includes list with specific ordering requirements.
List<String> _cryptoSubdirIncludes(BuildInput input) {
  final cryptoDir = Directory.fromUri(
    input.packageRoot.resolve('third_party/libressl/crypto/'),
  );
  const excludedCryptoDirs = {'hidden', 'compat', 'arch'};
  return cryptoDir
      .listSync()
      .whereType<Directory>()
      .where(
        (d) => !excludedCryptoDirs.contains(
          d.uri.pathSegments[d.uri.pathSegments.length - 2],
        ),
      )
      .where((d) => d.listSync().any((f) => f is File && f.path.endsWith('.h')))
      .map(
        (d) =>
            'third_party/libressl/crypto/${d.uri.pathSegments[d.uri.pathSegments.length - 2]}/',
      )
      .toList()
    ..sort(); // deterministic ordering
}

