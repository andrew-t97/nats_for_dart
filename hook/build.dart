// ---------------------------------------------------------------------------
// Platform-specific source exclusion
// ---------------------------------------------------------------------------
// LibreSSL's sources.txt contains compat files for ALL platforms. Most compat
// files have NO #ifndef HAVE_X guard — they define functions unconditionally.
// Compiling them on a platform that already provides the function causes
// duplicate symbols or compilation failures from missing platform headers.
//
// Files guarded by #ifndef HAVE_X (safe to always include, but unnecessary
// when HAVE_X is defined): bsd-asprintf.c, getdelim.c, getline.c
// ---------------------------------------------------------------------------

/// Returns true if [sourcePath] should be excluded for the given target.
bool _isExcludedSource(
  String sourcePath,
  OS targetOS,
  Architecture? targetArch,
) {
  final basename = sourcePath.split('/').last;

  // Exclude Windows-only files on non-Windows targets (they require
  // windows.h / ws2tcpip.h and won't compile on Unix). On Windows these
  // files are needed and must pass through.
  if (targetOS != OS.windows && _nonWindowsExcludedFiles.contains(basename)) {
    return true;
  }

  // Always exclude getopt_long.c — it redefines struct option from <getopt.h>
  // and conflicts on both macOS and Linux.
  if (basename == 'getopt_long.c') return true;

  // Exclude armcap.c — it references assembly probe functions (_armv7_neon_probe,
  // _armv8_aes_probe, etc.) that only exist in .S files. With OPENSSL_NO_ASM,
  // crypto_legacy.c provides an empty OPENSSL_cpuid_setup() stub instead.
  if (basename == 'armcap.c') return true;

  // Architecture-specific CPU caps: only compile the one for the target arch.
  // Each arch variant defines crypto_cpu_caps_init() (and ia32 variants add
  // crypto_cpu_caps_ia32()); compiling multiple causes duplicate symbols.
  if (sourcePath.contains('/crypto/arch/')) {
    final archDir = _archDirs[targetArch];
    if (archDir == null) return true; // unknown arch — exclude all
    return !sourcePath.contains('/arch/$archDir/');
  }

  // Platform-specific getentropy: exclude all variants except getentropy_win.c.
  // macOS/Linux: system libc provides getentropy (HAVE_GETENTROPY defined).
  // Windows: getentropy_win.c is needed; it passes the _nonWindowsExcludedFiles
  // check on non-Windows and is kept here on Windows.
  if (basename.startsWith('getentropy_') && basename != 'getentropy_win.c') {
    return true;
  }

  // Platform-specific getprogname: only keep the one for the target OS.
  if (basename.startsWith('getprogname_')) {
    return !_getprognameSources.containsKey(targetOS) ||
        basename != _getprognameSources[targetOS];
  }

  // Exclude compat files for functions the target OS already provides.
  // These files have no HAVE_X guard and will cause duplicate symbols.
  final platformExcludes = switch (targetOS) {
    OS.macOS => _macosExcludedCompat,
    OS.linux => _linuxExcludedCompat,
    OS.windows => _windowsExcludedCompat,
    _ => <String>{},
  };
  if (platformExcludes.contains(basename)) return true;

  return false;
}

/// HAVE_* feature macros common to macOS and Linux (both provide these via libc).
const _posixHaveDefines = {
  'HAVE_CLOCK_GETTIME': null,
  'HAVE_GETENTROPY': null,
  'HAVE_ASPRINTF': null,
  'HAVE_MEMMEM': null,
  'HAVE_GETPAGESIZE': null,
  'HAVE_STRCASECMP': null,
  'HAVE_STRNDUP': null,
  'HAVE_STRNLEN': null,
  'HAVE_STRSEP': null,
};

/// Maps Dart Architecture to LibreSSL's crypto/arch/ subdirectory name.
const _archDirs = {
  Architecture.arm64: 'aarch64',
  Architecture.x64: 'amd64',
  Architecture.ia32: 'i386',
};

/// Source files that are Windows-only (require windows.h / ws2tcpip.h)
/// and must be excluded when building for non-Windows targets.
const _nonWindowsExcludedFiles = {
  'b_win.c',
  'ui_openssl_win.c',
  'crypto_lock_win.c',
  'explicit_bzero_win.c',
  'getentropy_win.c',
  'getprogname_windows.c',
  'posix_win.c',
};

/// Platform-specific getprogname implementations.
const _getprognameSources = {
  OS.linux: 'getprogname_linux.c',
  OS.windows: 'getprogname_windows.c',
};

/// Compat files excluded on macOS because macOS provides these functions
/// natively (HAVE_X is defined) and the files have no guard.
const _macosExcludedCompat = {
  'arc4random.c', // HAVE_ARC4RANDOM_BUF
  'arc4random_uniform.c', // part of arc4random family
  'getpagesize.c', // HAVE_GETPAGESIZE
  'strcasecmp.c', // HAVE_STRCASECMP
  'strlcat.c', // HAVE_STRLCAT
  'strlcpy.c', // HAVE_STRLCPY
  'strndup.c', // HAVE_STRNDUP
  'strnlen.c', // HAVE_STRNLEN
  'strsep.c', // HAVE_STRSEP
  'timingsafe_bcmp.c', // HAVE_TIMINGSAFE_BCMP
};

/// Compat files excluded on Linux because Linux (glibc) provides these
/// functions natively (HAVE_X is defined) and the files have no guard.
const _linuxExcludedCompat = {
  'explicit_bzero.c', // HAVE_EXPLICIT_BZERO
  'getpagesize.c', // HAVE_GETPAGESIZE
  'strcasecmp.c', // HAVE_STRCASECMP
  'strndup.c', // HAVE_STRNDUP
  'strnlen.c', // HAVE_STRNLEN
  'strsep.c', // HAVE_STRSEP
};

/// Compat files excluded on Windows because MSVC/Win32 provides them natively.
/// Do NOT add explicit_bzero_win.c, getentropy_win.c, getprogname_windows.c,
/// or posix_win.c — Windows needs those compat implementations.
const _windowsExcludedCompat = {
  'strcasecmp.c', // n-win.h defines strcasecmp as _stricmp
  'getpagesize.c', // Windows: GetSystemInfo() provides page size
  'strndup.c', // available in MSVC 14+
  'strnlen.c', // available in MSVC (HAVE_STRNLEN defined)
  'timingsafe_bcmp.c', // not needed on Windows
};
