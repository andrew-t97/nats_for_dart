import 'package:hooks/hooks.dart';
import 'package:logging/logging.dart';
import 'package:native_toolchain_c/native_toolchain_c.dart';

void main(List<String> args) async {
  await build(args, _build);
}

Future<void> _build(BuildInput input, BuildOutputBuilder output) async {
  if (!input.config.buildCodeAssets) return;

  // Guard: ensure vendored source is present.
  final natsHeader = File.fromUri(
    input.packageRoot.resolve('third_party/nats_c/src/nats.h'),
  );
  if (!natsHeader.existsSync()) {
    throw StateError(
      'Vendored nats.c source not found at third_party/nats_c/.\n'
      'The third_party/nats_c/ directory should contain the nats.c source files.',
    );
  }

  final libresslDir = File.fromUri(
    input.packageRoot.resolve('third_party/libressl/sources.txt'),
  );
  if (!libresslDir.existsSync()) {
    throw StateError(
      'Vendored LibreSSL source not found at third_party/libressl/.\n'
      'Run scripts/update_libressl.sh to populate third_party/libressl/.',
    );
  }

  // Load LibreSSL source file list from sources.txt (generated by
  // scripts/update_libressl.sh from CMakeLists.txt).
  final allLibresslSources = libresslDir
      .readAsLinesSync()
      .where((line) => line.trim().isNotEmpty)
      .toList();

  final targetOS = input.config.code.targetOS;
  final targetArch = input.config.code.targetArchitecture;

  // Filter out platform-inappropriate source files.
  // sources.txt contains ALL platform compat files (Windows, AIX, HP-UX, etc.)
  // and all architecture variants — we only compile the ones needed for the
  // target OS and architecture.
  final libresslSources = allLibresslSources
      .where((path) => !_isExcludedSource(path, targetOS, targetArch))
      .toList();

  // Architecture-specific LibreSSL include paths.
  final archIncludes = switch (targetArch) {
    Architecture.arm64 => [
      'third_party/libressl/crypto/arch/aarch64/',
      'third_party/libressl/crypto/bn/arch/aarch64/',
    ],
    Architecture.x64 => [
      'third_party/libressl/crypto/arch/amd64/',
      'third_party/libressl/crypto/bn/arch/amd64/',
    ],
    Architecture.ia32 => ['third_party/libressl/crypto/arch/i386/'],
    _ => <String>[],
  };

  // Single-pass CBuilder: compile all 512 LibreSSL .c files + 43 NATS C .c
  // files together into one shared library. No pkg-config, no dynamic
  // libssl/libcrypto linking.
  final cbuilder = CBuilder.library(
    name: 'nats',
    assetName: 'src/nats_bindings.g.dart',
    std: targetOS == OS.windows ? 'c11' : 'gnu99',
    sources: [...libresslSources, ..._natsSourcesForOS(targetOS)],
    includes: [
      // Hidden namespace wrappers MUST come before include/ so that
      // #include <openssl/ssl.h> finds the wrapper (which uses #include_next
      // to chain to the public header and then imports LSSL_ALIAS/LCRYPTO_ALIAS).
      'third_party/libressl/ssl/hidden/',
      'third_party/libressl/crypto/hidden/',
      // Compat headers (string.h, stdlib.h, etc.) shadow system headers to
      // provide freezero, recallocarray, explicit_bzero, timingsafe_memcmp, etc.
      'third_party/libressl/include/compat/',
      // LibreSSL public headers (openssl/*.h) — found via #include_next from
      // the hidden wrappers above.
      'third_party/libressl/include/',
      // LibreSSL internal crypto headers (crypto_internal.h, etc.)
      'third_party/libressl/crypto/',
      // LibreSSL internal SSL headers
      'third_party/libressl/ssl/',
      // LibreSSL crypto subdirectory includes (cross-module #include "..._local.h").
      // Auto-discovered: all crypto/ subdirs containing .h files, excluding
      // hidden/, compat/, arch/ (handled separately above).
      ..._cryptoSubdirIncludes(input),
      // Architecture-specific headers
      ...archIncludes,
      // NATS C headers
      'third_party/nats_c/src/',
      'third_party/nats_c/src/include/',
    ],
    defines: {
      // --- NATS C defines ---
      '_REENTRANT': null,
      'NATS_HAS_TLS': null,
      'NATS_FORCE_HOST_VERIFICATION': null,

      // --- LibreSSL internal defines ---
      // OpenBSD visibility macros — must be empty on non-OpenBSD.
      '__BEGIN_HIDDEN_DECLS': '',
      '__END_HIDDEN_DECLS': '',
      // Gates internal APIs: CBS_dup, SYSerror, error macros, etc.
      'LIBRESSL_INTERNAL': null,
      // Gates additional crypto-internal APIs.
      'LIBRESSL_CRYPTO_INTERNAL': null,
      // No hardware padlock support.
      'OPENSSL_NO_HW_PADLOCK': null,
      // No assembly — pure C compilation.
      'OPENSSL_NO_ASM': null,
      // Default certificate directory path.
      'OPENSSLDIR': targetOS == OS.windows ? r'"C:\\OpenSSL"' : r'"/etc/ssl"',

      // --- Architecture CPU caps ---
      // Each crypto/arch/<arch>/crypto_cpu_caps.c provides crypto_cpu_caps_init()
      // (and ia32 variants provide crypto_cpu_caps_ia32()). These defines tell
      // crypto_legacy.c to skip its fallback stubs.
      'HAVE_CRYPTO_CPU_CAPS_INIT': null,
      if (targetArch == Architecture.x64 || targetArch == Architecture.ia32)
        'HAVE_CRYPTO_CPU_CAPS_IA32': null,

      // --- Platform HAVE_* feature macros ---
      if (targetOS == OS.macOS || targetOS == OS.linux) ..._posixHaveDefines,
      if (targetOS == OS.macOS) ...{
        // macOS-specific (BSD libc)
        'HAVE_STRLCPY': null,
        'HAVE_STRLCAT': null,
        'HAVE_ARC4RANDOM_BUF': null,
        'HAVE_TIMINGSAFE_BCMP': null,
        'HAVE_GETPROGNAME': null,
        'DARWIN': null,
      },
      if (targetOS == OS.linux) ...{
        // Linux-specific (glibc)
        'HAVE_EXPLICIT_BZERO': null,
        'HAVE_ENDIAN_H': null,
        'LINUX': null,
        '_GNU_SOURCE': null,
      },
      if (targetOS == OS.windows) ...{
        // Vista+ required for CONDITION_VARIABLE used by NATS C win/cond.c.
        '_WIN32_WINNT': '0x0600',
        // Trim rarely-used Windows headers to reduce build time and conflicts.
        'WIN32_LEAN_AND_MEAN': null,
        // Suppress MSVC unsafe-function warnings (sprintf, strcpy, etc.).
        '_CRT_SECURE_NO_WARNINGS': null,
        // MSVC provides strnlen natively.
        'HAVE_STRNLEN': null,
      },
    },
    libraries: [
      if (targetOS == OS.linux) 'pthread',
      if (targetOS == OS.windows) ...const [
        'ws2_32', // Winsock2 — required by NATS C win/sock.c
        'crypt32', // Windows CryptoAPI — required by LibreSSL
        'bcrypt', // Windows BCNG — used by LibreSSL getentropy_win.c
      ],
    ],
  );

  await cbuilder.run(
    input: input,
    output: output,
    logger: Logger('')
      ..level = Level.ALL
      ..onRecord.listen((record) => print(record.message)),
  );
}

